package de.artsem.springcourse.Project2Boot.models;

public class SystemType {

    private int id;
    private String name;

    public SystemType(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
